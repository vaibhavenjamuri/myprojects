package com.ars.model;

public class Payment {

	int paymentId;
	
	String type;
	
	float amount;
	
	String userName;
	
	String status;
	
	String paymentDate;
	
	String accountNo;
	
	String bankName;
	
	String month;
	
	String year;
	
	String cardNumber;
	
	String cardType;
	
	String expDate;
	
	String cvv;
	
	
	

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getCardNumber() {
		return cardNumber;
	}

	public void setCardNumber(String cardNumber) {
		this.cardNumber = cardNumber;
	}

	public String getCardType() {
		return cardType;
	}

	public void setCardType(String cardType) {
		this.cardType = cardType;
	}

	public String getExpDate() {
		return expDate;
	}

	public void setExpDate(String expDate) {
		this.expDate = expDate;
	}

	public String getCvv() {
		return cvv;
	}

	public void setCvv(String cvv) {
		this.cvv = cvv;
	}

	public final int getPaymentId() {
		return paymentId;
	}

	public final void setPaymentId(int paymentId) {
		this.paymentId = paymentId;
	}

	public final String getType() {
		return type;
	}

	public final void setType(String type) {
		this.type = type;
	}

	public final float getAmount() {
		return amount;
	}

	public final void setAmount(float amount) {
		this.amount = amount;
	}

	public final String getUserName() {
		return userName;
	}

	public final void setUserName(String userName) {
		this.userName = userName;
	}

	public final String getStatus() {
		return status;
	}

	public final void setStatus(String status) {
		this.status = status;
	}

	public final String getPaymentDate() {
		return paymentDate;
	}

	public final void setPaymentDate(String paymentDate) {
		this.paymentDate = paymentDate;
	}

	public final String getAccountNo() {
		return accountNo;
	}

	public final void setAccountNo(String accountNo) {
		this.accountNo = accountNo;
	}

	public final String getBankName() {
		return bankName;
	}

	public final void setBankName(String bankName) {
		this.bankName = bankName;
	}
	
	
}
